def get_nucleotide_matrix(match=1, mismatch=-1):
    """Simple match/mismatch matrix for DNA/RNA."""
    bases = "ATCG"
    return {(b1, b2): match if b1 == b2 else mismatch for b1 in bases for b2 in bases}